describe('createFileReader — directory listing', () => {
  it('listHeadDir returns directories only; ENOENT → null', async () => {
    const dir = await mkdtemp(join(tmpdir(), 'lq-'));
    try {
      await mkdir(join(dir, 'sub-a'));
      await mkdir(join(dir, 'sub-b'));
      await writeFile(join(dir, 'a-file.txt'), '');
      const fs = await import('node:fs/promises');
      const reader = createFileReader({
        cwd: dir,
        fs: {
          readFile: (p, e) => fs.readFile(p, e),
          readdir: (p, o) => fs.readdir(p, o),
        },
        exec: async () => ({ stdout: '', stderr: '', code: 0 }),
      });
      const entries = await reader.listHeadDir('.');
      expect(entries?.sort()).toEqual(['sub-a', 'sub-b']);
      expect(await reader.listHeadDir('missing')).toBeNull();
    } finally {
      await rm(dir, { recursive: true, force: true });
    }
  });

  it('listAtRefDir parses git ls-tree output, strips path prefix, returns null on absent path', async () => {
    const reader = createFileReader({
      cwd: '/x',
      fs: {
        readFile: async () => '',
        readdir: async () => [],
      },
      exec: async (cmd, args) => {
        expect(cmd).toBe('git');
        expect(args).toEqual(['ls-tree', '-d', '--name-only', 'main:apps/']);
        return { stdout: 'apps/a\napps/b\n', stderr: '', code: 0 };
      },
    });
    expect(await reader.listAtRefDir('main', 'apps')).toEqual(['a', 'b']);
  });

  it('listAtRefDir returns null when git reports missing path', async () => {
    const reader = createFileReader({
      cwd: '/x',
      fs: {
        readFile: async () => '',
        readdir: async () => [],
      },
      exec: async () => ({
        stdout: '',
        stderr: "fatal: not a tree object 'main:nope/'",
        code: 128,
      }),
    });
    expect(await reader.listAtRefDir('main', 'nope')).toBeNull();
  });

  it('listAtRefDir throws when ref is unresolvable', async () => {
    const reader = createFileReader({
      cwd: '/x',
      fs: {
        readFile: async () => '',
        readdir: async () => [],
      },
      exec: async () => ({
        stdout: '',
        stderr: "fatal: bad revision 'nope'",
        code: 128,
      }),
    });
    await expect(reader.listAtRefDir('nope', 'apps')).rejects.toThrow(/bad revision/);
  });

  it('listAtRefDir omits trailing slash when listing tree root', async () => {
    const reader = createFileReader({
      cwd: '/x',
      fs: {
        readFile: async () => '',
        readdir: async () => [],
      },
      exec: async (_cmd, args) => {
        expect(args).toEqual(['ls-tree', '-d', '--name-only', 'main:']);
        return { stdout: 'apps\nsrc\n', stderr: '', code: 0 };
      },
    });
    expect(await reader.listAtRefDir('main', '.')).toEqual(['apps', 'src']);
  });
});
